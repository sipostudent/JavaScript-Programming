// Lesson 2: Strings and Variables

// Create a variable to store the city
let city = 'Birmingham'

// Create a variable to store the County
let county = 'West Midlands'

// Create a variable to combine the city and county
let location = city + ', ' + county

// Print the combined location to the terminal
console.log(location)


